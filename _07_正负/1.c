#include <stdio.h>

int main()
{
 	unsigned int num1,num2;
 	num1 = 10;
 	num2 = 60;
 	printf("10 - 60\n\n\n");  
	printf("10-60 = %d\n",10-60);  
	printf("num1 - num2 = %d\n\n",num1 - num2);  
	
	printf("(unsigned int)(10-60) = %d\n",(unsigned int)(10-60));  
	printf("(unsigned int)(num1 - num2) = %d\n",(unsigned int)(num1 - num2)); 
	
	while(1);
	return 0;
}
